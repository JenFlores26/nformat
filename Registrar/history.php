  <center><img src="CvSU/dfcam.jpg" style="width:1048px; height: 865px; border:5px solid;border-color:#020cbd; "></center>
  <center><p style="margin-top: 0;">Photo of DFCAMCLP Building</p></center>

  <p style="font-size: 20px;margin-left: 100px;margin-right: 100px;">• DFCAMCLP started as a special project of <strong>former Mayor Vergel "Nene" A. Aguilar </strong>backed up and supported by his wife, the incumbient <strong>City Mayor Imelda T. Aguilar. </strong>City Ordinance <strong>No. 352-97 Series of 1997 </strong>was enacted on <strong>August 13, 1997 </strong>establishing DFCAMC.</p><br><br>

  <center><img src="CvSU/dr.png" style="width: 380px; height: 400px;border:5px solid;border-color:#020cbd;"></center>
  <center><p style="margin-top: 0;">Photo of Dr. Filemon C. Aguilar</p></center>

  <p style="font-size: 20px;margin-left: 100px;margin-right: 100px;text-align: justify;">The school was named in honor of his father namely,<strong> Dr. Filemon C. Aguilar</strong>. In <strong>March 12, 1998</strong>, the inauguration of a campus is also the birthdate of DFCAMCLP Founder Former Mayor <strong>Imelda "Nene" Aguilar</strong>. DFCAMC began to offer courses of <strong>Bachelor of Science in Business Administration Major in Management, Marketing, Entrepreneurship, Economics and Banking and Finance</strong> in <strong>March 12, 1998</strong> with a total of <strong>1,083</strong> enrolled from various secondary schools. In <strong>October 7, 1998</strong>, the issuance of <b>City Accordance No. 417-A-98</b> correct the spelling of Felimon to Filemon. The College Offered a new program, the <b>Bachelor of Science in Accountancy</b>. It is complimented it's other program, the <b>Bachelor of Science in Business Accountancy</b> strengthening DFCAMC’s mandate to become a full-blown school of business. In a year <b>2000</b>, the school affiliated with <b>American Civil Liberties Union</b> making its presence felt across the region. The college disproved the elitist notion that quality education is the monopoly with expensive private colleges and universities. The first commencement exercises was held at Aguilar Sports Complex in Pilar Village, Las Pinas City in <b>April 5, 2002</b> with <b>558</b> graduates. In October 2003, DFCAMCLP produced its first three CPA passers with 50% passing rate. In April 2009, the City Ordinance No. 887-09 Series of 2009 changed the name of the school to <b>Dr. Filemon C. Aguilar Memorial College of Las Pinas (DFCAMCLP)<b>. </p><br><br>

